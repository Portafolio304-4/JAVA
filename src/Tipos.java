    private void btnAgregarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTipoActionPerformed
        TipoDAO tipo = new TipoDAO();
        String nombre = txtNombreTipo.getText();
        String codTipo = txtCodigoTipo.getText();
        String codTipoCat = cmbTipoCategoria.getModel().getSelectedItem().toString();
        
        boolean validNom = true;
        boolean validCod = true;
        if(codTipo.length()< 3){
            JOptionPane.showMessageDialog(null, "el codigo debe tener al menos 3 caracters");
            validCod = false;
        }
        else{
        }
        if(nombre.length()< 5){
            JOptionPane.showMessageDialog(null, "el nombre debe tener al menos 5 caracters");
            validNom = false;
        }
        
        if(validNom && validCod){
            TipoCategoriaDAO categoriaSearch = new TipoCategoriaDAO();
            TipoCategoriaDTO categoria = new TipoCategoriaDTO();
            
            try {
                categoria = categoriaSearch.getByCode(codTipoCat);
                boolean insertionResult = false;
                try {
                    insertionResult = tipo.add(codTipo, nombre, categoria.getId() );
                } catch (SQLException ex) {
                    Logger.getLogger(Tipos.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println(insertionResult);
                if(insertionResult){
                    try {
                        this.loadTableTipos();
                    } catch (SQLException ex) {
                        Logger.getLogger(Tipos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(null, "Tipo agregado con exito!");
                    System.out.println("Tipo agregado con exito!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Tipos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_btnAgregarTipoActionPerformed

    private void btnEliminarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTipoActionPerformed
        // TODO add your handling code here:
        int selected_row = tableTipo.getSelectedRow();
        if (selected_row != -1){
            String codTipo = tableTipo.getModel().getValueAt(selected_row, 0).toString();
            TipoDAO tipo = new TipoDAO();
            try {
                boolean result = tipo.disabledByCodTipo(codTipo);
                if (result){
                    JOptionPane.showMessageDialog(null, "Tipo eliminado con exito!");
                    loadTableTipos();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Tipos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarTipoActionPerformed

