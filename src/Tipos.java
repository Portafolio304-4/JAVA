    public void loadTableTipos() throws SQLException{
        TipoDAO tipo = new TipoDAO();
        List<TipoDTO> listaTipos = tipo.getAllWithCat();
        DefaultTableModel model = (DefaultTableModel) tableTipo.getModel();   
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        for (TipoDTO item : listaTipos) {
            System.out.println(item.getCodTipo());
            System.out.println(item.getNombre());
            Object[] row = { item.getCodTipo(), item.getNombre(), item.getCategoria().getCodTipoCat() };
            model.addRow(row);
            
        }
    }
    public void loadCmbTipoCategoria() throws SQLException{
        TipoCategoriaDAO categoria = new TipoCategoriaDAO();
        List<TipoCategoriaDTO> listaTipos = categoria.getAll();
        cmbTipoCategoria.removeAllItems();
        for (TipoCategoriaDTO item : listaTipos) {
            cmbTipoCategoria.addItem(item.getCodTipoCat());
        }
    }

